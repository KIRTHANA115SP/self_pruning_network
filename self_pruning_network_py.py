class PrunableLinear(nn.Module):
    def __init__(self, in_features: int, out_features: int, bias: bool = True):
        super().__init__()
        self.in_features  = in_features
        self.out_features = out_features

        # ── Standard weight & bias (same init as nn.Linear) ──────────────────
        self.weight = nn.Parameter(torch.empty(out_features, in_features))
        self.bias   = nn.Parameter(torch.empty(out_features)) if bias else None

        # ── Gate scores – same shape as weight ───────────────────────────────
        # Initialised near 0 so sigmoid ≈ 0.5; the network starts "half-open"
        # and learns to either open (→ 1) or close (→ 0) each gate.
        self.gate_scores = nn.Parameter(torch.empty(out_features, in_features))

        self._reset_parameters()

    def _reset_parameters(self):
        # Kaiming uniform (same as default nn.Linear)
        nn.init.kaiming_uniform_(self.weight, a=math.sqrt(5))
        if self.bias is not None:
            fan_in, _ = nn.init._calculate_fan_in_and_fan_out(self.weight)
            bound = 1 / math.sqrt(fan_in) if fan_in > 0 else 0
            nn.init.uniform_(self.bias, -bound, bound)
        # Gate scores: small random values → sigmoid starts close to 0.5
        nn.init.normal_(self.gate_scores, mean=0.0, std=0.01)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        gates         = torch.sigmoid(self.gate_scores)   # ∈ (0, 1)
        pruned_weight = self.weight * gates               # element-wise
        return F.linear(x, pruned_weight, self.bias)      # gradients flow ✓

    def get_gates(self) -> torch.Tensor:
        """Return current gate values (detached, for monitoring)."""
        return torch.sigmoid(self.gate_scores).detach()

    def extra_repr(self) -> str:
        return (f"in_features={self.in_features}, "
                f"out_features={self.out_features}, "
                f"bias={self.bias is not None}")

class SelfPruningNet(nn.Module):
    def __init__(self, hidden_dims=(1024, 512, 256)):
        super().__init__()

        input_dim = 3 * 32 * 32   # 3072

        layers = []
        prev = input_dim
        for h in hidden_dims:
            layers.append(PrunableLinear(prev, h))
            layers.append(nn.BatchNorm1d(h))
            layers.append(nn.ReLU(inplace=True))
            layers.append(nn.Dropout(0.3))
            prev = h

        self.features   = nn.Sequential(*layers)
        self.classifier = PrunableLinear(prev, 10)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = x.view(x.size(0), -1)   # flatten
        x = self.features(x)
        return self.classifier(x)

    def prunable_layers(self):
        """Yield all PrunableLinear sub-modules."""
        for module in self.modules():
            if isinstance(module, PrunableLinear):
                yield module

def sparsity_loss(model: SelfPruningNet) -> torch.Tensor:
    total = torch.tensor(0.0, device=next(model.parameters()).device)
    for layer in model.prunable_layers():
        gates = torch.sigmoid(layer.gate_scores)   # keep in graph!
        total = total + gates.sum()
    return total

import torchvision
